function e = GloloSaccade(varargin)
    e = Experiment(varargin{:});
    
    e.trials.base = SimpleSaccadeTrial...
        ( 'targetLoc', CircularMotion ...
            ( 'radius', 16 ...
            , 'omega', 0 ...
            )...
        , 'fixationTime', Inf ...
        , 'fixationOnset', 0.4 ...
        , 'fixationLatency', 1.0 ...
        , 'fixationStartWindow', 3 ...
        , 'fixationSettle', 0.4 ...
        , 'fixationWindow', 1.5 ...
        , 'targetBlankColor', 0.4875 ...
        , 'maxLatency', 0.5 ...
        , 'maxTransitTime', 0.1 ...
        , 'targetWindow', 4 ...
        , 'rewardSize', 100 ...
        , 'rewardTargetBonus', 0.0 ...
        , 'errorTimeout', 0.1 ...
        );
    
    e.trials.add('targetLoc.phase', UniformDistribution('lower', 0, 'upper', 2*pi));
    e.trials.add('targetLoc.omega', UniformDistribution('lower', -15/8, 'upper', 15/8));
    
    %target onset hazard from fixation
    e.trials.add('targetOnset', ExponentialDistribution('offset', 0.0, 'tau', 0.15));
    e.trials.add('targetBlank', ExponentialDistribution('offset', 0.2, 'tau', 0.15));
    e.trials.add('targetBlankColor', DiscreteStaircase...
        ( 'valueSet', linspace(127.5,121.5,4)/255 ... %stepping up the contrast is down the intensity
        , 'currentIndex', 1 ...
        , 'Nup', 1, 'Ndown', 3 ...
        ) );
    e.trials.add('cueTime', ExponentialDistribution('offset', 0.3, 'tau', 0.15));
    
    %target tracking time
    e.trials.add('targetFixationTime', ExponentialDistribution('offset', 0.4, 'tau', 0.2));
    
    
    %begin with an eye calibration and again every 10 minutes...
    e.trials.blockSize = 200;
    e.trials.blockTrial = EyeCalibrationMessageTrial();
    e.trials.blockTrial.base.absoluteWindow = 100;
    e.trials.blockTrial.base.maxLatency = 0.5;
    e.trials.blockTrial.maxStderr = 0.5;
    e.trials.blockTrial.minCalibrationInterval = 900;
    e.trials.blockTrial.base.fixDuration = 0.5;
    e.trials.blockTrial.base.fixWindow = 4;
    e.trials.blockTrial.base.rewardDuration = 100;
    e.trials.blockTrial.base.settleTime = 0.3;
    e.trials.blockTrial.base.targetRadius = 0.4;
    e.trials.blockTrial.base.targetInnerRadius = 0.2;
    e.trials.blockTrial.base.onset = 0;
    e.trials.blockTrial.interTrialInterval = 0.5;
    e.trials.blockTrial.targetX = -15:5:15;
    e.trials.blockTrial.targetY = -15:5:15;

    %before 
end
