function e = CircleInterpolation(varargin)
    params = struct...
        ( 'skipFrames', 1  ...
        , 'requireCalibration', 1 ...
        , 'continuing', 0 ...
        );
    params = namedargs(params, varargin{:});
    
    dx = 0.75;
    dt = 0.15;
    baseOnset = 0.5;
    n = 1;
    barDelay = 0.3;
    
    generator = CircleInterpolationTrialGenerator...
        ( 'base', CircleInterpolationTrial...
            ( 'patch', CauchyPatch...
                ( 'velocity', dx/dt ...
                , 'size', [0.5 0.75 0.1]...
                , 'order', 4 ...
                ) ...
            , 'radius', 10 ...
            , 'dx', dx ...
            , 'dt', dt ...
            , 'n', n ...
            , 'comparisonBarOnset', baseOnset + n * dt + barDelay ...
        )...
        , 'nTargets', 5 ...
        , 'minGap', 2*pi/8 ...
        , 'onsetAsynchronies', linspace(-0.5*dt, (n-0.5)*dt, 3*(n+1) + 1)...
        , 'interpolatedDisplacements', linspace( -0.5*dx, 1.5*dx, 5) ...
        );
    
    e = Experiment('trials', generator, params);
    e.run();
end