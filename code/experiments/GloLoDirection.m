        , 'skipFrames', 1  ...
        , 'requireCalibration', 0 ...
        , 'priority', 9 ...
        , 'hideCursor', 0 ...
        , 'diagnostics', 0 ...
        );
    params = namedargs(params, varargin{:});
    
    e = Experiment...
        ( 'continuing', 0 ...
        , 'trials', GloLoDirectionTrialGenerator...
            ( 'base', GloLoDirectionTrial...
                ( 'cueOnset', 1 ...
                , 'fixationPointSize', 0.1 ...
                , 'fixationPointShift', 0.05 ...
                , 'fixationPointShiftPhase', 0 ... %must specify this manually.
                , 'fixationPointShiftDuration', 0.1 ...
