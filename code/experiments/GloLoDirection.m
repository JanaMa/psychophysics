                    ( 'size', [1/2 0.5 0.1] ...
                    , 'velocity', 5 ... 
                    ) ...
                , 'motion', CircularMotionProcess...
                    ( 'radius', 8 ...
                    , 'n', 3 ...
                    , 't', 1 ...
                    , 'phase', 0 ...
                    , 'dt', 0.15 ...
                    , 'dphase', 0.75 / 10 ... %dx = 0.75...
                    , 'angle', 90 ...
                    , 'color', [0.125;0.125;0.125] ...
                    )...
                )...
            , 'blocksize', 100 ...
            , 'factors', struct...
                ( 'nTargets', [1 2 3 4 5] ...
