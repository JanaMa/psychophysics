function this = CircleDemo(varargin)
    radius = 10;
    wavelength = 0.75;
    tfreq = 8;

    %we're going to show a ring with a second order motion going around
    %the outside of it...

    radii = repmat(radius, 1, 2);
    clambda = wavelength * [1 -1];

    grating = SimpleCircularGratingSource...
              ( 'loc', repmat([0;0], 1, 2) ...
              , 'lobes', round(radii * 2 * pi ./ round(clambda)) ...
              , 'omega', tfreq * 2 * pi ...
              , 'radius', radii ...
              , 'width', 0.375 ...
              , 'color', repmat([1;1;1], 1, 2)/8 ...
              );

    n = 8;
    m_radii = repmat([9 9 11 11], 1, n);
    mlambda = wavelength * m_radii ./ radius;
    mspeed = tfreq .* mlambda;
    dt = 0.125;

    leftward = 0.25;
    rightward = 0.25;

    motion = CircularCauchyMotion ...
       ( 'radius', m_radii ...
       , 't', repmat(0, 1, 4*n) ...
       , 'dt', dt ...
       , 'x', 0 ...
       , 'y', 0 ...
       , 'dphase', mspeed ./ m_radii * dt ...
       , 'angle', 90 + ...
         (reshape(repmat(0:(n-1), 4, 1), 1, []) ...
          .* 360/n) ...
       , 'phase', (reshape(repmat(0:(n-1), 4, 1), 1, []) .* 2*pi/n) ...
       , 'color', repmat([leftward rightward leftward rightward], 3, n) ...
       , 'velocity', tfreq .* mlambda .* repmat([1 -1 1 -1], 1, n) ...
       , 'wavelength', mlambda ...
       , 'width', 0.5 ...
       , 'duration', dt ...
       );

    fixation = FilledDisk([0;0], 0.1, 'visible', 1);

    kb_ = KeyDown();
    t_ = Trigger();
    drawer1_ = CauchySpritePlayer('process', motion);
    drawer2_ = CircularGratingDrawer('source', grating);

    persistent init__;
    this = autoobject(varargin{:});

    function params = run(params)
        main = mainLoop...
            ( 'input', {params.input.keyboard} ...
            , 'graphics', {fixation, drawer2_, drawer1_} ...
            , 'triggers', {kb_, t_} ...
            );

        kb_.set(main.stop, 'ESCAPE');
        t_.singleshot(always(), @start)

        params = main.go(params);
    end

    function k = start(k)
        drawer1_.setVisible(1, k.next);
        drawer2_.setVisible(1, k.next);
        fixation.setVisible(1, k.next);
    end

    %    playDemo(this, 'skipFrames', 0, varargin{:});
    playDemo(this, varargin{:});
end
